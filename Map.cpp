void Map::Update()
{
	player.Move();
	enemy.Move();

	//collision
	if (CheckCollisionRecs(player.rec, enemy.rec))
	{
		std::cout << "COLLISION DETECTED!!!!!!!!!!!!!" << std::endl;
	}
	else
	{
		std::cout << "0" << std::endl;
	}


	if (player.position.x > 800.0f)
	{
		player.position = { 0, player.position.y };
		locationState.x++;
	}

	else if (player.position.x < -0.1f && locationState.x != 0)
	{
		player.position = { 800, player.position.y };
		locationState.x--;
	}
	
	else if (player.position.y > 600.0f)
	{
		player.position = { player.position.x, 0 };
		locationState.y++;
	}

	else if (player.position.y < -0.1f && locationState.y != 0)
	{
		player.position = { player.position.x, 600 };
		locationState.y--;
	}
}



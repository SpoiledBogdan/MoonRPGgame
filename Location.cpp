void Location::DrawLocation(std::vector<int> location)
{
	int j = 0;

	for (float y = 0; y < settings::mapHeight; y += settings::tileHeight)
	{
		for (float x = 0; x < settings::mapWidth; x += settings::tileWidth)
		{
			if (j < location.size())
			{
				Vector2 newPos = { x, y };

				switch (location[j])
				{
				case 0:
					tileMap.push_back(ground1);
					break;
				case 1:
					tileMap.push_back(ground2);
					break;
				case 2:
					tileMap.push_back(test);
					break;
				}

				tileMap.back().setPos(newPos);
				tileMap.back().Draw();
				j++;
			}
		}
	}
}


